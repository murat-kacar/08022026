const navItems = [
  { href: '/admin/dashboard', label: 'Dashboard', icon: Icons.dashboard, section: 'ana' },
  { href: '/admin/events', label: 'Etkinlikler', icon: Icons.events, section: 'içerik' },
  { href: '/admin/instructors', label: 'Eğitmenler', icon: Icons.instructors, section: 'içerik' },
  { href: '/admin/applications', label: 'Başvurular', icon: Icons.applications, section: 'içerik' },
  { href: '/admin/pages', label: 'Sayfa İçerikleri', icon: Icons.pages, section: 'yönetim' },
  { href: '/admin/settings', label: 'Site Ayarları', icon: Icons.settings, section: 'yönetim' },
];

const pageTitles: Record<string, string> = {
  '/admin/dashboard': 'Dashboard',
  '/admin/events': 'Etkinlikler',
  '/admin/instructors': 'Eğitmenler',
  '/admin/applications': 'Başvurular',
  '/admin/pages': 'Sayfa İçerikleri',
  '/admin/settings': 'Site Ayarları',
};

export default function AdminShell({ username, children }: { username: string; children: React.ReactNode }) {
  const pathname = usePathname();
  const router = useRouter();
  const [sidebarOpen, setSidebarOpen] = useState(false);

  const pageTitle = Object.entries(pageTitles).find(([path]) => pathname.startsWith(path))?.[1] ?? 'Admin';

  const handleLogout = async () => {
    await fetch('/api/auth/logout', { method: 'POST' });
    router.push('/admin/login');
  };

  const isActive = (href: string) => {
    if (href === '/admin/dashboard') return pathname === '/admin/dashboard';
    return pathname.startsWith(href);
  };

  let currentSection = '';

  return (
    <div className="min-h-screen" style={{ background: '#f8fafc' }}>
      {/* Mobile overlay */}
      {sidebarOpen && (
        <div
          className="fixed inset-0 bg-black/40 z-30 md:hidden"
          onClick={() => setSidebarOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside className={`admin-sidebar ${sidebarOpen ? 'open' : ''}`}>
        <div className="admin-sidebar-brand">
          <div className="admin-sidebar-brand-icon">HK</div>
          <div>
            <h2>HK Akademi</h2>
            <span>Yönetim Paneli</span>
          </div>
        </div>

        <nav>
          {navItems.map((item) => {
            const showSection = item.section !== currentSection;
            if (showSection) currentSection = item.section;

            return (
              <React.Fragment key={item.href}>
                {showSection && (
                  <div className="admin-nav-section">
                    {item.section === 'ana' ? 'Genel' : item.section === 'içerik' ? 'İçerik Yönetimi' : 'Sistem'}
                  </div>
                )}
                <Link
                  href={item.href}
                  className="admin-nav-link"
                  onClick={() => setSidebarOpen(false)}
                  style={
                    isActive(item.href)
                      ? { background: 'rgba(255,255,255,0.1)', color: '#f1f5f9', fontWeight: 550 }
                      : {}
                  }
                >
                  {item.icon}
                  {item.label}
                </Link>
              </React.Fragment>
            );
          })}
        </nav>

        <div className="admin-sidebar-footer">
          <a href="#" onClick={(e) => { e.preventDefault(); handleLogout(); }}>
            {Icons.logout}
            <span>Çıkış Yap</span>
          </a>
        </div>
      </aside>

      {/* Main area */}
      <div className="admin-main">
        <header className="admin-topbar">
          <div style={{ display: 'flex', alignItems: 'center', gap: '0.75rem' }}>
            <button
              className="admin-btn-icon admin-mobile-toggle"
              onClick={() => setSidebarOpen(!sidebarOpen)}
              aria-label="Menüyü aç"
            >
              {sidebarOpen ? Icons.close : Icons.menu}
            </button>
            <h1>{pageTitle}</h1>
          </div>
          <div className="admin-topbar-actions">
            <Link
              href="/"
              target="_blank"
              className="admin-btn admin-btn-secondary admin-btn-sm"
              style={{ gap: '0.35rem' }}
            >
              {Icons.globe}
              Siteyi Gör
            </Link>
            <div style={{ width: 32, height: 32, borderRadius: '50%', background: '#e2e8f0', display: 'flex', alignItems: 'center', justifyContent: 'center', fontSize: '0.75rem', fontWeight: 600, color: '#475569' }}>
              {username.charAt(0).toUpperCase()}
            </div>
          </div>
        </header>

        <div className="admin-content">
          {children}
        </div>
      </div>
    </div>
  );
}
